import heapq
hq = []
n = int(input())
for _ in range(n):
    nums = list(map(int, input().split()))
    for num in nums:
        heapq.heappush(hq, num)
        while len(hq) > n:
            heapq.heappop(hq)
print(heapq.heappop(hq))


